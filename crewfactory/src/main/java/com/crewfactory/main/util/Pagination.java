package com.crewfactory.main.util;

public class Pagination {
	
	private int starter;
	private int ender;
	private int total;
	private int size;
	
	public Pagination() {}

	public int getStarter() {
		return starter;
	}

	public void setStarter(int starter) {
		this.starter = starter;
	}

	public int getEnder() {
		return ender;
	}

	public void setEnder(int ender) {
		this.ender = ender;
	}

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}
	
	

}
